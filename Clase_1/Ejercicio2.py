num1 = float(input("Ingrese el primer numero: "))
num2 = float(input("Ingrese el segundo numero: "))

#Operaciones
suma = num1 + num2
resta = num1 - num2
division = num1 / num2
multiplicacion = num1 * num2

#Salida de datos
print(f"Suma: {suma}")
print(f"Resta: {resta}")
print(f"Division: {division}")
print(f"Multiplicacion: {multiplicacion}")